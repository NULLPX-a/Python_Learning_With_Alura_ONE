# learning python basics with alura one courses, starting with variables and data types
# defining a variable
name = "Alice"
age = 30
height = 5.5
is_student = False

# printing variable values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# demonstrating data types
print("Data type of name:", type(name))
print("Data type of age:", type(age))
print("Data type of height:", type(height))
print("Data type of is_student:", type(is_student))

# location of variables in memory
print("Memory location of name:", id(name))
print("Memory location of age:", id(age))
print("Memory location of height:", id(height))
print("Memory location of is_student:", id(is_student))

# updating variable values
name = "Bob"
age = 25
height = 6.0
is_student = True

print("\nUpdated Values:")
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

